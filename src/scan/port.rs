//!     pub port: u16,
//!     pub protocol: protocol,
//! }
//!
//! """)
//!    NMAP_SERVICES = "/usr/share/nmap/nmap-services"
//!    ALLOWED_PROTOS = ("tcp", "udp")
//!    with open(NMAP_SERVICES) as services:
//!        lines = services.readlines()
//!    i = 0
//!    while lines[i].startswith("#"):
//!        i += 1
//!    ports_info = []
//!    def parse(line):
//!        fields = line.split("#")[0].rstrip().split('\t')
//!        port, proto = fields[1].split('/')
//!        freq = fields[2]
//!        return int(port), proto, float(freq)
//!    while i < len(lines):
//!        port, proto, freq = parse(lines[i])
//!        i += 1
//!        if proto in ALLOWED_PROTOS:
//!            ports_info.append((port, [proto], [freq]))
//!            break
//!    for line in lines[i+1:]:
//!        port, proto, freq = parse(line)
//!        if proto not in ALLOWED_PROTOS:
//!            continue
//!        prev = ports_info[-1]
//!        if prev[0] == port:
//!            prev[1].append(proto)
//!            prev[2].append(freq)
//!        else:
//!            ports_info.append((port, [proto], [freq]))
//!    ports_info = sorted(
//!        sorted(
//!            ports_info,
//!            key=lambda t: sum(t[2]),
//!            reverse=True
//!        )[:1000],
//!        key=lambda t: t[0]
//!    )
//!    out.write("pub const COMMON_PORTS: &[CommonPortInfo] = &[\n")
//!    for port, proto, _ in ports_info:
//!        protocol = proto[0].capitalize() if len(proto) == 1 else "Both"
